package pro.sky.java.course1.lesson1;

public class Author {
    private String name;
    private String famely;

    public Author(String name, String famely) {
        this.name = name;
        this.famely = famely;
    }

    public String getName() {
        return this.name;
    }

    public String getFamely() {
        return this.famely;

    }
}
